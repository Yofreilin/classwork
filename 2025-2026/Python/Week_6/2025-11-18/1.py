def main():

    print(multiply_two(3, 6))
    print(add_two(4, 7))
    print(subtract_two(3, 8))
    print(divide_two(4, 8))
    print()

def multiply_two(first, second):
    return first * second

def add_two(first, second):
    return first + second

def subtract_two(first, second):
    return first - second

def divide_two(first, second):
    return first / second

if __name__ == '__main__':
    main()